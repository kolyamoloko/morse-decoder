function decode(expr) {
    const arr = [];//add array
    for (let i = 0; i < expr.length; i += 10) {arr.push(expr.slice(i, i + 10));} // add multiple of 1
    return arr.map(arr =>MORSE_TABLE[arr.replace(/^0+/, '').replace(/11/g, '-').replace(/10/g, '.')]).join('');//change symbols for morse-decoder
}
